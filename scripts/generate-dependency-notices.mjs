import { readFile, writeFile } from 'node:fs/promises'

const rootPackage = JSON.parse(await readFile('package.json', 'utf8'))
const lock = JSON.parse(await readFile('package-lock.json', 'utf8'))
const packages = lock.packages ?? {}

const runtimePackagePaths = collectRuntimePackagePaths(rootPackage.dependencies ?? {})
const rows = [...runtimePackagePaths]
  .map((path) => {
    const pkg = packages[path]
    return {
      name: packageNameFromPath(path),
      version: pkg.version ?? 'unknown',
      license: pkg.license ?? 'UNKNOWN',
      resolved: pkg.resolved ?? '',
    }
  })
  .sort((a, b) => a.name.localeCompare(b.name))

const missingLicenseRows = rows.filter((row) => row.license === 'UNKNOWN')
const output = [
  '# Third Party Notices',
  '',
  'Generated by `npm run notices` from `package-lock.json`.',
  '',
  'This file lists runtime npm packages used by the browser app. Development-only test/build packages are intentionally excluded.',
  '',
  '| Package | Version | License | Source |',
  '| --- | --- | --- | --- |',
  ...rows.map((row) => `| \`${row.name}\` | \`${row.version}\` | ${row.license} | ${row.resolved || '-'} |`),
  '',
  missingLicenseRows.length > 0
    ? `Packages missing license metadata: ${missingLicenseRows.map((row) => `\`${row.name}\``).join(', ')}.`
    : 'All listed runtime packages include license metadata in the lockfile.',
  '',
].join('\n')

await writeFile('docs/THIRD_PARTY_NOTICES.md', output)

function collectRuntimePackagePaths(rootDependencies) {
  const queue = Object.keys(rootDependencies)
  const seenNames = new Set()
  const seenPaths = new Set()

  while (queue.length > 0) {
    const name = queue.shift()
    if (!name || seenNames.has(name)) {
      continue
    }
    seenNames.add(name)
    const path = findPackagePath(name)
    if (!path) {
      throw new Error(`Package ${name} was not found in package-lock.json`)
    }
    seenPaths.add(path)
    const pkg = packages[path]
    for (const dependencyName of Object.keys(pkg.dependencies ?? {})) {
      queue.push(dependencyName)
    }
  }

  return seenPaths
}

function findPackagePath(name) {
  const directPath = `node_modules/${name}`
  if (packages[directPath]) {
    return directPath
  }
  return Object.keys(packages).find((path) => path.endsWith(`/node_modules/${name}`))
}

function packageNameFromPath(path) {
  const packagePath = path.startsWith('node_modules/')
    ? path.slice('node_modules/'.length)
    : path.split('/node_modules/').at(-1) ?? path
  const segments = packagePath.split('/')
  if (segments[0]?.startsWith('@')) {
    return `${segments[0]}/${segments[1]}`
  }
  return segments[0] ?? path
}
